	La Gestión documental engloba un conjunto de operaciones comprometidas con la búsqueda de la economía y la eficacia en la producción, el mantenimiento, uso y destino final de los documentos a lo largo de su ciclo de vida; es decir, desde el momento de su concepción en las oficinas administrativas hasta su ingreso en las instituciones de archivo
	</div>
	
	<div class="paragrah" style="margin-left: 30px;margin-right: 30px;">
	Gestión del Conocimiento y Gestión Documental
	Sin dudas, desde la Antigüedad las personas han registrado sus ideas, comportamientos, actividades a través de documentos en diferentes soportes. De esta manera se han establecido diferentes medios para transmitir información. Sin embargo los soportes en que se registra esa información se han diversificado y han evolucionado hasta el presente. Cada vez se hace más necesario conservar la documentación valiosa que permita a individuos y organizaciones desempeñarse cada vez mejor y enfrentar los incesantes cambios del entorno. 
	</div>
	
	<div class="paragrah" style="margin-left: 30px;margin-right: 30px;">
	El conocimiento se ha convertido entonces en un recurso intangible de gran valor para el desarrollo de las organizaciones, las que se enfrentan a una fuerte competencia condicionada por cambios tecnológicos y de la sociedad. Su gestión es un proceso que presenta paricularidades que lo distinguen de la gestión de recursos tangibles, dado por el hecho de que el conocimiento, por su naturaleza, tiene como bases para su producción y difusión los procesos de información y comunicación. Para la implantación de un sistema de gestión del conocimiento se requiere desarrollar una adecuada gestión de las tecnologías, de los recursos humanos y de la información registrada en soportes documentales; de manera que ese conocimiento le permita a la organización captar los cambios del entorno y adaptarse a ellos. 
	</div>
	
	<div class="paragrah" style="margin-left: 30px;margin-right: 30px;">
	La implantación de sistemas de gestión del conocimiento prioriza la necesidad de integrar los sistemas de gestión de la información (sistemas de gestión documental, gestores de bases de datos, intranets y extranets, herramientas de búsqueda y recuperación de la información y tecnologías para la colaboración entre grupos de trabajo), de manera tal que los usuarios puedan acceder de forma unificada a la información que necesitan, por ello, es difícil implantar un sistema de gestión del conocimiento sin la existencia de uno de los pilares de la gestión de la información: el sistema de gestión documental o archivo, en tanto factor indispensable para la organización y utilización de los recursos de información internos de las organizaciones y empresas. 
	</div>
	
	<div class="paragrah" style="margin-left: 30px;margin-right: 30px;">
	Ciclo de vida de los documentos

	El concepto de ciclo de vida de los documentos, se basa en la concepción de records management, no considera al archivo histórico en la fase final de la vida del documento, ni la hace coincidir con el esquema de la estructura orgánica del sistema archivístico. Este enfoque ha generado, en los últimos años una nueva concepción denominada archivística integrada o archivos totales que abarca el tratamiento de la documentación desde sus orígenes en el proceso administrativo hasta su conservación definitiva, lo que implica aglutinar el ciclo de vida, el análisis de las necesidades de las administraciones, el establecimiento de un calendario de conservación, el diseño racional de los documentos, la protección de los documentos esenciales, la organización y el tratamiento de los documentos, su eliminación o transferencia a los archivos definitivos. 
	</div>
	
	<div class="paragrah" style="margin-left: 30px;margin-right: 30px;">
	De manera general se considera que el documento de archivo transita por tres edades:

    * Edad activa: documentos con vigencia administrativa y con un alto grado de uso. Se conservan en los archivos de gestión o de oficina.
    * Edad semiactiva: los que continúan vigentes pero resultan de poca consulta, y se transfieren a un archivo general.
    * Edad inactiva: documentos que han perdido su vigencia administrativa. Tienen dos posibles destinos: el archivo histórico para los documentos con valores permanentes y la destrucción para aquellos que no poseen ninguna valía. 

	Esta concepción, que ha alcanzado fuerza en los últimos años, otorga integridad a la archivística, complementando las herramientas de la gestión documental con las del tratamiento tradicional de la documentación, propio de los archivos de documentos permanentes. En Cuba,la gestión documental ha tenido un insuficiente desarrollo. Los diferentes censos, diagnósticos e investigaciones realizados por el Archivo Nacional de Cuba, han mostrado la escasa existencia de archivos en nuestras administraciones y la acumulación de grandes volúmenes documentales sin ningún tipo de tratamiento. 
	</div>
	
	<div class="paragrah" style="margin-left: 30px;margin-right: 30px;">
	Por esta razón, buena parte de los archivos administrativos existentes no pasan de ser depósitos de papel, completamente ajenos a las necesidades informativas de las organizaciones y concebidos en función de la conservación de los documentos para su utilización sólo como fuentes de investigación histórica. Esta situación se hace compleja con el acelerado proceso de introducción de las nuevas tecnologías de la información y las comunicaciones, que nos enfrentará con un nuevo y delicado problema: el acceso y gestión de la información contenida en los documentos electrónicos, producidos en los más variados formatos, que puede derivar en la aparición de nuevos “almacenes”, con defectos mucho más nocivos para las organizaciones que los de papel, por su carácter virtual.

	La archivística cubana se ha centrado en la concepción de los archivos al servicio de la investigación, la historia y la cultura; prueba de ello es la escasa producción bibliográfica relacionada con temas tales como la gestión documental y la implantación de sistemas de gestión documental o programas de administración de documentos para las organizaciones. Los derroteros por donde hoy marcha la archivística, como hemos explicado, pueden significar una excelente oportunidad para emprender el camino de la transformación en nuestro país. Sobre todo si sabemos aprovechar las posibilidades de integración del tratamiento documental de los documentos activos, semiactivos y permanentes.

	Cualquier intento de implementar sistemas de gestión documental en nuestras organizaciones debe realizarse sobre bases archivísticas sólidas y en estrecha colaboración entre gestores de información, archiveros y especialistas en tecnologías de la información. Pues, según muestran diversas experiencias internacionales, en muchos casos estos programas no alcanzan una completa efectividad porque han sido diseñados por informáticos e ingenieros que han tenido en cuenta, fundamentalmente, aspectos relacionados con la disminución de los costos de almacenamiento y conservación, en un claro desbalance con su objetivo esencial: la recuperación de la información para ser utilizada por los miembros de la organización. 
